# Membership (in, not in) -  Checks for the existence of a value.

tup1 = (10, 20, 30, 40, 50, 10, 10)  
tup2 = (11, 22, 33, 44, 55)

# Check if an element exists in the tuple using the 'in' keyword.
print("Is 22 in tup1?", 22 in tup1)  # Output: False
print("Is 22 in tup2?", 22 in tup2)  # Output: True
print()

t1 = (1, 2, 3)
# Check if an element does not exists in the tuple using the 'not in' keyword.
print(5 not in t1)  # Output: True
print()
