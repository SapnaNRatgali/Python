# The index() method returns the position of the first occurrence of a specified element in the tuple.

my_tuple = (1, 2, 3, 2)
index = my_tuple.index(2)      # Result: index = 1
print("The index of the first occurrence of 2 in my_tuple is:", index)
print()

# Another example
tup1=(10,20,30,40,50,10,10)
print(tup1)
print()
print(f"The element 20 in tup1 is at position:", tup1.index(20))
print()
