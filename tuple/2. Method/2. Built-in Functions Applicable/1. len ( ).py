# len(tuple) - Returns the length of the tuple.

# Using len() method


# Creating a tuple with parentheses
tup1 = (10, 20, 30, 40, 50, 10, 10)
print("Tuple 1:", tup1)
print()

# Creating a tuple without parentheses
tup2 = 11, 22, 33, 44, 55
print("Tuple 2:", tup2)
print()

print("The length of tup2 is:", len(tup2))  # 5 elements
print("The length of tup1 is:", len(tup1))  # 7 elements
print()
