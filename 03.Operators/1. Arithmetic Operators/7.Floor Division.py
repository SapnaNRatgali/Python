# Floor Division : " // "
# it divides two numbers and returns the largest integer that is less than or equal to the result, effectively rounding down to the nearest whole number.

print(10 // 3)   # (quotient of 10/3, rounded down)
print()

print(20 // 6)   
print()

a = 20
b = 25
print(b // a) # Floor Division (integer result): 1
print()
